void handleKeypress(unsigned char key, int x, int y) {
    switch (key) {
        case 'a': // Stop all movements
            carSpeed = 0.0f;
            SkySpeed = 0.0f;
            trainSpeed = 0.0f;
            Speed = 0.0f;
            break;

        case 'w': // Start or speed up movements
            carSpeed = 2.0f;
            SkySpeed = 2.0f;
            trainSpeed = 2.0f;
            Speed = 2.0f;
            break;

        case 'p': // Slow down movements
            carSpeed = 0.5f;
            SkySpeed = 0.1f;
            Speed = 0.5f;
            trainSpeed = 0.5f;
            break;

        case 'd': // Toggle to day scene

            isDay = true;
           // isDay222 = true;
            glutPostRedisplay(); // Request to redraw the window
            break;

            case 'D':
            //isDay = true;
            isDay222 = true;
            glutPostRedisplay(); // Request to redraw the window
            break;

        case 'n': // Toggle to night scene
            isDay = false;
           // isDay222 = true;
            glutPostRedisplay();
            break;

            case 'N':
            //isDay = false;
            isDay222 = false;
            glutPostRedisplay();
            break;

        case 'b': // Stop specific Boat and sky speeds
            carSpeed1 = 0.0f;
            SkySpeed1 = 0.0f;
            trainSpeed = 0.0f;
            Speed = 0.0f;
            break;

        case 'x': // Increase specific specific Boat and sky speeds
            carSpeed1 = 2.0f;
            SkySpeed1 = 2.0f;
            trainSpeed = 2.0f;
            Speed = 2.0f;
            break;

        case 'q': // Slow down specific Boat and sky speeds
            carSpeed1 = 0.5f;
            SkySpeed1 = 0.1f;
            Speed = 0.5f;
            trainSpeed = 0.5f;
            break;

        case 'c': // Switch to Village
            isCity = false;
            glutPostRedisplay();
            break;

        case 'C': // Switch to City
            isCity = true;
            glutPostRedisplay();
            break;

        default:
            break;
    }
}

